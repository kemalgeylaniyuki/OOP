package com.kemalgeylaniyuki.oopproject;

public class Mathematics {

    // Static Polymorphism

    public int sum() { // Sadece 0 döndüren bir metod
        return 0;
    }

    public int sum(int x, int y){ // Basit bir toplama fonksiyonu
        return x + y;
    }

    public int sum(int x, int y, int z){
        return x + y + z;
    }




}
